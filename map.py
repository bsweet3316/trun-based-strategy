class Map:
    def __init__(self, screen):
        self.map = MAP
        self.screen = screen

    def drawMap(self):
        for i in range(0, len(self.map)): 
            for j in range(0, len(self.map[i])):
                if self.map[i][j] == "-":
                    pygame.draw.rect(self.screen, (0,0,0),((j*40), (i*40), 40, 40))
                elif self.map[i][j] == ".":
                    pygame.draw.rect(self.screen, (255,255,255), ((j*40), (i*40), 40, 40))
                elif self.map[i][j] == "P":
                    self.drawBasePlayer(i,j)
                else:
                    pygame.draw.rect(self.screen, (0,255,0), ((j*40), (i*40), 40, 40))
    
    
    
    def setMapValue(self, x, y, value):
        self.map[x][y] = value
        
    def drawBasePlayer(self, i, j):
        pygame.draw.rect(self.screen, (255,255,255), ((j*40), (i*40), 40, 40))
        pygame.draw.circle(self.screen, (255,0,0), ((i*40) + 20, (i*40) + 20), 20)
        
        
